// ---------------------------------------------------------------------------------------------------------------------
// Imports
// ---------------------------------------------------------------------------------------------------------------------
using Microsoft.Extensions.ObjectPool;

namespace AterraCore.Common.Types;
// ---------------------------------------------------------------------------------------------------------------------
// Code
// ---------------------------------------------------------------------------------------------------------------------
public readonly struct DisposablePooledResource<T>(ObjectPool<T> pool) : IDisposable where T : class {
    public T Item { get; } = pool.Get();
    public void Dispose() => pool.Return(Item);
}
