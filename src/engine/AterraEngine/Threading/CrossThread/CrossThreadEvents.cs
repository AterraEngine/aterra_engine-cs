[UsedImplicitly]
public class CrossThreadEvents : ICrossThreadEvents {
    public event EventHandler? OnCloseApplication;
    
    // -----------------------------------------------------------------------------------------------------------------
    // Methods
    // -----------------------------------------------------------------------------------------------------------------
    public void InvokeCloseApplication(object? sender = null) => OnCloseApplication?.Invoke(sender ?? this, EventArgs.Empty);
}
